//지수 연산자

console.log(2 ** 2);
console.log(2 ** 2.5);
console.log(2 ** 0);
console.log(2 ** -2);

console.log("---------------------------------------");

//Math.pow 메서드

console.log(Math.pow(2, 2));
console.log(Math.pow(2, 2.5));
console.log(Math.pow(2, 0));
console.log(Math.pow(2, -2));

console.log("---------------------------------------");

